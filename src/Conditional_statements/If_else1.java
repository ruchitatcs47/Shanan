public class If_else1 {
	public static void main(String[] args) {
		int boyAge=22;
		int girlAge=18;
		if(boyAge>=22 & girlAge>=18) {
			System.out.println("eligible for marriage");
		}
		else {
			System.out.println("not eligible for marriage");
		}
	}

}
